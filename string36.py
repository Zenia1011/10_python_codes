#formatting methods
a = 8
b = 6

print ('{} + {} = {}'.format(a,b,a+b))
print ('{} - {} = {}'.format(a,b,a-b))
print ('{} * {} = {}'.format(a,b,a*b))
print ('{} / {} = {}'.format(a,b,a/b))
print ('{} % {} = {}'.format(a,b,a%b))
print ('{} // {} = {}'.format(a,b,a//b))
print ('{} ** {} = {}'.format(a,b,a**b))
