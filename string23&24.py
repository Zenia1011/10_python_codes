#Use index or find to find the position of the first and rfind to find the last occurrence of the word 'because' in the following sentence: 'You cannot end a sentence with because because because is a conjunction'
x = 'you cannot end a sentence with because because because is a connjunction'
print (x)
print ("the position of the first occurence of the word 'because' is: ",x.find('because'))
print ("the position of the last occurence of the word 'because' is: ",x.rfind('because'))